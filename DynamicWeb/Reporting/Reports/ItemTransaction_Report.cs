    private void Time_BeforePrint(object sender, CancelEventArgs e)
    {
        DateTime utcNow = DateTime.UtcNow;
        TimeZoneInfo info = TimeZoneInfo.FindSystemTimeZoneById("Egypt Standard Time");
        DateTime cTime = TimeZoneInfo.ConvertTimeFromUtc(utcNow, info);
        ((XRLabel)sender).Text = cTime.ToString("d MMMM yyyy h:mm tt");
        if (this.ItemId.Value != null)
        {
            var itemName = db.Items.Where(a => a.IsActive == true && a.IsDeleted == false && a.Id == ((int)this.ItemId.Value)).FirstOrDefault().ArName;
            this.xrLabel2.Text =itemName;
        }
        else
        {
            this.xrLabel2.Text = " ";
        }
    }

